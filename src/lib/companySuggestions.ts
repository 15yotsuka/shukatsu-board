export function getCompanySuggestions(query: string): CompanySuggestion[] {
  const trimmed = query.trim();
  if (!trimmed) return [];
  return COMPANY_NAMES
    .filter((name) => name.startsWith(trimmed))
    .slice(0, 5)
    .map((name) => ({ name, ...deriveIndustry(name) }));
}

